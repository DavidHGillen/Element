/**
 * Hold and track final button values
 */
class InputState extends Evee {
	// static
	////////////////////////////////////////////////////////////////////////////
	static get UPDATE() { return "InputState.Update"; }

	// ctor
	////////////////////////////////////////////////////////////////////////////
	constructor() {
		super();
	}

	// core
	////////////////////////////////////////////////////////////////////////////
	updateState(data) {
		this.emit(InputState.UPDATE, data);
	}
}